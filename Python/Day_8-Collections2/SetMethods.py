A = {10,20,30}
B = {30,40,50}
# add()
A.add(60)
print(A)
# update()
A.update([70,80])
print(A)
# remove()
A.remove(20)
print(A)
# discard()
A.discard(100)
print(A)
# pop()
A.pop()
print(A)
# union()
print(A.union(B))
# intersection()
print(A.intersection(B))
# difference()
print(A.difference(B))
# symmetric_difference()
print(A.symmetric_difference(B))
# copy()
C = A.copy()
print(C)
# clear()
C.clear()
print(C)