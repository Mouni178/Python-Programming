fruits={"Apple","Orange","Guava","Watermelon","DragonFruit","Banana"}
print(fruits)
# Add an element
fruits.add("Pineapple")
print(fruits)
# Remove an element
fruits.remove("Banana")
print(fruits)
# Discard (No error if element not found)
fruits.discard("Watermelon")
print(fruits)
# Clear
temp = {1,2,3}
temp.clear()
print(temp)