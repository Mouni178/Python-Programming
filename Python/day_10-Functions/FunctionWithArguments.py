#Function with One Argument
def greet(name):
    print("Hello",name)
greet("Mounika")
greet("Jayanth")
print()
#Function with Two Arguments
def add(a,b):
    print(a+b)
add(10,30)
add(14,8)
print()
#Function with Three Arguments
def student(name,age,college):
    print(name)
    print(age)
    print(college)
student("Mounika",21,"KARE")
print()
#Return Statement
def add(a,b):
    return a+b
result = add(10,20)
print(result)